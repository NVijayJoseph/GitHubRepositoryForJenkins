public class C01_VariableDemo {
	public static void main(String[] args) {
		int age = 25;
		String name = "Vijay";

		System.out.println("Age: " + age);
		System.out.println("Name: " + name);
	}
}

/*
Topic: Variables
Project: Java_02_Variables_DataTypes
Package: p01_variables
Class: C01_VariableDemo

Purpose:
To understand what a variable is in Java.

Explanation:
- Variable is used to store data
- int age = 25; -> stores integer value
- String name = "Vijay"; -> stores text value
- println displays the stored values

Output:
Age: 25
Name: Vijay
*/